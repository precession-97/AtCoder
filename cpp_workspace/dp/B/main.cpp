template<class T> inline bool chmin(T& a, T b);

void solve(long long N, long long K, std::vector<long long> h){
    const long long INF = 1LL << 60;
    // DP table
    long long dp[N];
    // DP table init
    for(int i = 0; i < N; i++) {
        dp[i] = INF;
    }
    // initial condition
    dp[0] = 0;
    // calc min cost from dp[1] to dp[N-1]
    for(int i = 1; i < N; i++) {
        for(int k = 1; k <= K; k++) {
            if(k <= i) {
                // h[i-k] -> h[i]
                chmin(dp[i], dp[i-k] + abs(h[i-k] - h[i]));
            }
        }
    }
    cout << dp[N-1] << endl;
}

