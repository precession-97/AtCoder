long long calcGCD(long long x, long long y) {
    while(x >= 1 && y >= 1) {
        if(x > y) x = x % y;
        else y = y % x;
    }
    if(x > 0) return x;
    else return y;
}

void solve(long long N, std::vector<long long> A){
    long long gcd = A[0];
    for(int n = 1; n < N; ++n) {
        gcd = calcGCD(gcd, A[n]);
    }
    cout << gcd << endl;
}

